public class Arithmatic {
    public static void main(String[] args) {
        double x = 6.5;
        double y = 5.5;
        System.out.println("x : "+x);
        System.out.println("y : "+y);
        System.out.println("x == y : "+( x == y));
        System.out.println("x<y : "+(x<y));
        System.out.println("x>y : "+(x>y));
        System.out.println("x!=y : "+(x!=y));
        System.out.println("x<=y : "+(x<=y));
        System.out.println("!(x<=y) : "+(!(x<=y)));
        System.out.println("x>=y : "+(x>=y));
    }
}
